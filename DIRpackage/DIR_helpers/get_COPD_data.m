         0.645, 0.645, 2.5; ...
         0.652, 0.652, 2.5; ...
         0.590, 0.590, 2.5; ...
         0.647, 0.647, 2.5; ...
         0.633, 0.633, 2.5; ...
         0.625, 0.625, 2.5; ...
         0.586, 0.586, 2.5; ...
         0.664, 0.664, 2.5; ...
         0.742, 0.742, 2.5; ...
    ]; 

volsz = volszs_s(idx, :);
spc = spc_s(idx, :);
bpath = sprintf('%scopd%d/copd%d/', copd_path, idx, idx);
% bpath = sprintf('/media/valera/lin/DIR_MODEL/COPD/copd%d/copd%d/', idx, idx);
p_ve = sprintf('%scopd%d_eBHCT.img', bpath, idx);
p_vi = sprintf('%scopd%d_iBHCT.img', bpath, idx);
vole = read_dir_img(p_ve, volsz);
voli = read_dir_img(p_vi, volsz);        
voli = double(voli);       
vole = double(vole);

p_ve = sprintf('%scopd%d_300_eBH_xyz_r1.txt', bpath, idx);
fid = fopen(p_ve, 'r');
data = textscan(fid, '%f %f %f', 'headerlines', 0);
fclose(fid);
ptse = [data{1}, data{2}, data{3}];        

p_vi = sprintf('%scopd%d_300_iBH_xyz_r1.txt', bpath, idx);
fid = fopen(p_vi, 'r');
data = textscan(fid, '%f %f %f', 'headerlines', 0);
fclose(fid);
ptsi = [data{1}, data{2}, data{3}];        
end
