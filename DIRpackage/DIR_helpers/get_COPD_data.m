            512, 512, 102;...
            512, 512, 126;...
            512, 512, 126;...
            512, 512, 131;...
            512, 512, 119;...
            512, 512, 112;...
            512, 512, 115;...
            512, 512, 116;...
            512, 512, 135;...
            ];
